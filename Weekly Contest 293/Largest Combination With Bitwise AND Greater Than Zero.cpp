int largestCombination(vector<int>& cs) {
    int res = 0, max_e = *max_element(begin(cs), end(cs));
    for (int b = 1; b <= max_e; b <<= 1) {
        int cnt = 0;
        for (auto n : cs)
            cnt += (n & b) > 0;
        res = max(res, cnt);
    }
    return res;
}
